# while Loop

i = 1
while i < 6:
    print (i)
    i += 1
print("while loop executed successfully")

# break statement
# to stop loop execution
i = 1
while i < 6:
    print(i)
    i += 1
    if i == 3:
        break
print("demo for break statement")

#continue statement
i = 1
while i < 6:
    print(i)
    i += 1
    if i == 3:
        continue
print("demo for continue statement")
